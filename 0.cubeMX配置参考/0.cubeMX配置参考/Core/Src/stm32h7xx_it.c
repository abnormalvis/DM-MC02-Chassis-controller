/* C body for HardFault — receives the correct stacked frame pointer */
void HardFault_Body(uint32_t *sp)
{
  /* Stacked frame: [0]=R0 [1]=R1 [2]=R2 [3]=R3 [4]=R12 [5]=LR [6]=PC [7]=xPSR */
  printf("!HF PC=0x%08lX LR=0x%08lX R0=0x%08lX\r\n",
         (unsigned long)sp[6], (unsigned long)sp[5], (unsigned long)sp[0]);
  printf("   CFSR=0x%08lX HFSR=0x%08lX BFAR=0x%08lX\r\n",
         *(volatile unsigned long*)0xE000ED28UL,
         *(volatile unsigned long*)0xE000ED2CUL,
         *(volatile unsigned long*)0xE000ED38UL);
  while (1)
  {
    LED1_Toggle;
    HAL_Delay(200);
  }
}

/**
  * @brief This function handles Hard fault interrupt.
  *
  * Uses a naked function so no compiler prologue moves SP before we read it.
  * Selects MSP or PSP based on EXC_RETURN bit 2 to get the correct stacked frame.
  */
__attribute__((naked)) void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  __asm volatile(
      "TST   LR, #4          \n"  /* EXC_RETURN bit2: 0=Handler/MSP, 1=Thread/PSP */
      "ITE   EQ              \n"
      "MRSEQ R0, MSP         \n"
      "MRSNE R0, PSP         \n"
      "B     HardFault_Body  \n"
  );
  /* USER CODE END HardFault_IRQn 0 */
}

